public class a
{

    // the Bicycle class has three fields
    protected int a;
    protected int b;
    protected int c;

    // the Bicycle class has one constructor
    public a ( int aa, int bb, int cc )
    {
        b = cc;
        a = aa;
        c = bb;
    }

    // the Bicycle class has four methods
    public void a ( int aa )
    {
        a = aa;
    }

    public void b ( int aa )
    {
        b = aa;
    }

    public void c ( int aa )
    {
        c -= aa;
    }

    public void d ( int aa )
    {
        c += aa;
    }

}